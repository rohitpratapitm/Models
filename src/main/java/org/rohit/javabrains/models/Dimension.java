package org.rohit.javabrains.models;

public class Dimension {

	private int w;
	private int h;
	private String resize;
	
	public int getW() {
		return w;
	}
	public void setW(int w) {
		this.w = w;
	}
	public int getH() {
		return h;
	}
	public void setH(int h) {
		this.h = h;
	}
	public String getResize() {
		return resize;
	}
	public void setResize(String resize) {
		this.resize = resize;
	}
	
	
}
